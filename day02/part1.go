func vsScore(myHand, oppHand int) int {
	score := 0

	// Draw
	if myHand == oppHand {
		score += 3
		score += myHand
		return score
	}

	// Win
	if (myHand == Rock && oppHand == Scissors) ||
		(myHand == Paper && oppHand == Rock) ||
		(myHand == Scissors && oppHand == Paper) {
		score += 6
	}

	score += myHand
	return score
}

